    private void tabelPetaniMouseClicked() {
    int baris = tabelPetani.getSelectedRow();
    // Pastikan ada baris yang terpilih
    if (baris != -1) { 
        // Mengisi fields dari JTable
        txtIdPetani.setText(tabelPetani.getValueAt(baris, 0).toString());
        txtNamaPetani.setText(tabelPetani.getValueAt(baris, 1).toString());
        txtAlamat.setText(tabelPetani.getValueAt(baris, 2).toString());
        txtNoHp.setText(tabelPetani.getValueAt(baris, 3).toString());
        
        // Kunci ID agar tidak diubah saat proses UPDATE
        txtIdPetani.setEnabled(false);
    }
}
    private void tampilDataPetani() {
    // Definisikan Model untuk JTable
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Petani");
    model.addColumn("Nama");
    model.addColumn("Alamat");
    model.addColumn("No. HP");

    try {
        // Ambil data dari DAO
        java.sql.ResultSet rs = tblpetani.getAllPetani(); 
        
        // Isi Model dengan data
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_petani"),
                rs.getString("nama_petani"),
                rs.getString("alamat"),
                rs.getString("no_hp")
            });
        }
        
        // Set Model ke JTable
        tabelPetani.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saat mengisi tabel: " + e.getMessage());
    }
}

