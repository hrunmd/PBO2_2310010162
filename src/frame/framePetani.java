    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    String id = txtIdPetani.getText(); // Ambil ID dari field
    
    // [1] Validasi Input
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data di tabel untuk dihapus!");
        return;
    }
    
    // [2] Konfirmasi Penghapusan (Wajib sebelum menghapus data)
    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus data ID " + id + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            // [3] Panggil DAO DELETE
            tblpetani.hapusAnggota(id);
            
            // [4] REFRESH JTABLE
            tampilDataPetani(); 
            
            // [5] BERSIHKAN FIELDS dan BUKA KUNCI ID
            txtIdPetani.setText(null);
            txtNamaPetani.setText(null);
            txtAlamat.setText(null);
            txtNoHp.setText(null);
            txtIdPetani.setEnabled(true); // Membuka kunci ID setelah hapus
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat hapus: " + e.getMessage());
        }
    }

    }//GEN-LAST:event_btnHapusActionPerformed

