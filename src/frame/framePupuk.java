// =========================================================
private void tampilDataPupuk() {
    // PERHATIAN: Tambahkan import javax.swing.table.DefaultTableModel di bagian atas file!
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Pupuk");
    model.addColumn("Nama Pupuk");
    model.addColumn("Jenis"); 
    model.addColumn("Dosis (kg/ha)");

    try {
        java.sql.ResultSet rs = objekPupuk.getAllPupuk(); 
        
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_pupuk"),
                rs.getString("nama_pupuk"),
                rs.getString("jenis_pupuk"),
                rs.getDouble("dosis_per_ha_kg") // Ambil sebagai Double
            });
        }
        tabelPupuk.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error saat mengisi tabel: " + e.getMessage());
    }
}
