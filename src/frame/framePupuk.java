    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String id = txtIdPupuk.getText();
    String nama = txtNamaPupuk.getText();
    String jenis = cmbJenisPupuk.getSelectedItem().toString();
    
    double dosis = 0.0;
    try {
        dosis = Double.parseDouble(txtDosis.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Dosis harus berupa angka!");
        return;
    }

    if (id.isEmpty() || nama.isEmpty() || jenis.equals("-- Pilih Jenis --")) {
        JOptionPane.showMessageDialog(this, "ID, Nama, dan Jenis wajib diisi!");
        return;
    }
    
    objekPupuk.SimpanAnggota(id, nama, jenis, dosis);
    
    // REFRESH TABEL DAN BUKA KUNCI
    tampilDataPupuk(); 
    txtIdPupuk.setEnabled(true);
    
    // Kosongkan fields
    txtIdPupuk.setText("");
    txtNamaPupuk.setText("");
    txtDosis.setText("");
    cmbJenisPupuk.setSelectedIndex(0);

    
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String id = txtIdPupuk.getText();
    String nama = txtNamaPupuk.getText();
    String jenis = cmbJenisPupuk.getSelectedItem().toString();
    
    double dosis = 0.0;
    try {
        dosis = Double.parseDouble(txtDosis.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Dosis harus berupa angka!");
        return;
    }
    
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan ID Pupuk yang akan diubah!");
        return;
    }
    
    objekPupuk.ubahAnggota(id, nama, jenis, dosis);
    
    // REFRESH TABEL DAN BUKA KUNCI
    tampilDataPupuk();
    txtIdPupuk.setEnabled(true);
    
    // Opsional: Bersihkan fields
    txtIdPupuk.setText("");
    
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String id = txtIdPupuk.getText();
    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Masukkan ID Pupuk yang akan dihapus!");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus data ID " + id + "?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (konfirmasi == JOptionPane.YES_OPTION) {
         objekPupuk.hapusAnggota(id);
         
         // REFRESH TABEL DAN BUKA KUNCI
         tampilDataPupuk();
         txtIdPupuk.setEnabled(true);
         
         // Kosongkan fields
         txtIdPupuk.setText("");
         txtNamaPupuk.setText("");
         txtDosis.setText("");
         cmbJenisPupuk.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

