private void tabelPupukMouseClicked() {
    int baris = tabelPupuk.getSelectedRow();
    if (baris != -1) { 
        // Mengisi fields dari JTable
        txtIdPupuk.setText(tabelPupuk.getValueAt(baris, 0).toString());
        txtNamaPupuk.setText(tabelPupuk.getValueAt(baris, 1).toString());
        
        // Mengisi JComboBox Jenis Pupuk (Index 2)
        cmbJenisPupuk.setSelectedItem(tabelPupuk.getValueAt(baris, 2).toString());
        
        txtDosis.setText(tabelPupuk.getValueAt(baris, 3).toString());
        
        // Kunci ID (PK)
        txtIdPupuk.setEnabled(false);
    }
}
